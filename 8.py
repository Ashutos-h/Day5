list=[]
l=int(input("Enter the length of your list:"))
for i in range(l):
	item=int(input("Enter "+str(i+1)+" number"))
	list.append(item)
num=int(input("Enter number to be searched in list:"))
if num in list:
	list.remove(num)
print("List after deletion")
for i in list:
	print(str(i),end=' ')
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
